#include "task_manager.hpp"

void task_manager::update (int ms)
{
    task_iter i = m_tasks.begin();
    while (i != m_tasks.end())
	if ((*i)->m_is_finished) {
	    delete *i;
	    i = m_tasks.erase(i);
	} else {
	    if (!(*i)->m_is_paused)
		(*i)->update(ms);
	    i++;
	}
}
