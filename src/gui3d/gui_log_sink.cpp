#include "gui_log_sink.hpp"

using namespace std;
using namespace CEGUI;

void gui_log_sink::dump (psynth::base::log& l, int level, const string& msg)
{
    m_buffer += msg;
    m_buffer += "\n";
    if (m_window)
	m_window->setText (m_buffer);
}
